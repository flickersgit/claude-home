  function draw(ctx, avatarId, x, y, size) {
    const av = AVATARS[avatarId] || AVATARS[0];
    const px = size / 16;
    for (let row = 0; row < 16; row++) {
      for (let col = 0; col < 16; col++) {
        const ci = av.pixels[row * 16 + col];
        if (ci === 0) continue;
        ctx.fillStyle = av.colors[ci];
        ctx.fillRect(x + col * px, y + row * px, Math.ceil(px), Math.ceil(px));
      }
    }
  }

  function drawToCanvas(avatarId, size) {
    const canvas = document.createElement('canvas');
    canvas.width = size;
    canvas.height = size;
    canvas.style.imageRendering = 'pixelated';
    const ctx = canvas.getContext('2d');
    draw(ctx, avatarId, 0, 0, size);
    return canvas;
  }

  function getName(avatarId) {
    return (AVATARS[avatarId] || AVATARS[0]).name;
  }

  window.ArcadeAvatars = { draw, drawToCanvas, getName, count: AVATARS.length };
